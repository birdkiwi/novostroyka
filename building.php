        <p>На территории жилого комплекса предусмотрено строительство трех 16-ти этажных дома и административного здания. Расположение домов относительно друг друга обеспечивает полную изоляцию от автомобилей. Строительство ведется посредством монолитного каркаса с кирпичным заполнением.</p>

        <p>В двух трехподъездных домах предусмотрено по 398 квартир, площадь которых от 28 до 58 кв. метров. Третий одноподъездный жилой дом отличается повышенной комфортностью. Здесь предлагается 63 квартиры, в число которых входят двухкомнатное и трехкомнатное жилье площадью от 64 до 75 кв. метров.</p>

        <p>Новый микрорайон «Стрижи на Родниках» возводится на ул. Мясниковой вблизи ул. Тюленина и ул. Курчатова. В непосредственной близости имеются остановки с общественным транспортом. Для жителей доступны маршрутные такси и автобусы. «Стрижи» на улице Мясниковой расположены в 20 минутах от станции метро «Заельцовская».</p>

        Жилой комплекс строится в районе с развитой удобной инфраструктурой. Вблизи микрорайона «Стрижи на Родниках» расположены «Лента» и «Ашан». В шаговой доступности имеются аптеки, кафе, магазины, детские сады, поликлиники, автозаправки и другие необходимые объекты инфраструктуры. На территории будут оборудованы спортивные зоны и детские игровые площадки. Во дворах предусмотрено видеонаблюдение и охрана.</p>

        <p>Микрорайон «Стрижи - Родники» возводится ГК «Стрижи», в состав которой входят застройщики ИСК «Мочище» и ООО «Квартал». За годы работы ГК Стрижи уже реализовала несколько комплексов на ул. Кубовой. В домах, построенных ГК «Стрижи», уже живут более 8000 семей. Застройщик уделяет особое внимание социальной ответственности, организовывая спортивные, дошкольные и культурные мероприятия.</p>
